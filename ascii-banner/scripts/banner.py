FONTS = {
    "block": BLOCK_FONT,
    "slim": SLIM_FONT,
    "shadow": SHADOW_FONT,
    "dos": DOS_FONT,
    "mini": MINI_FONT,
    "digital": DIGITAL_FONT,
}

FONT_DESCRIPTIONS = {
    "block": "Classic block letters using # characters (5 rows)",
    "slim": "Compact single-width characters (3 rows)",
    "shadow": "3D shadow-style with depth (4 rows)",
    "dos": "Retro DOS/BIOS style (6 rows)",
    "mini": "Ultra-compact two-row font (2 rows)",
    "digital": "Seven-segment display style (3 rows)",
}


def render_text(text, font_name="block"):
    """Render text using the specified font."""
    font = FONTS.get(font_name)
    if not font:
        raise ValueError(f"Unknown font: {font_name}. Available: {', '.join(FONTS.keys())}")

    # Uppercase for fonts that only have uppercase
    if font_name in ("block", "shadow", "dos"):
        text = text.upper()
    else:
        text = text.upper()

    # Get number of rows from first defined character
    sample = next(iter(font.values()))
    num_rows = len(sample)

    # Build each row
    lines = []
    for row in range(num_rows):
        line = ""
        for char in text:
            if char in font:
                line += font[char][row]
            else:
                line += " " * len(sample[0])
        lines.append(line.rstrip())

    return lines


def add_border(lines, border_char="*", padding=1):
    """Wrap rendered text in a border."""
    max_width = max(len(line) for line in lines)
    pad = " " * padding
    inner_width = max_width + padding * 2
    total_width = inner_width + 2

    bordered = []
    bordered.append(border_char * total_width)
    for _ in range(padding):
        bordered.append(border_char + " " * inner_width + border_char)
    for line in lines:
        padded = pad + line.ljust(max_width) + pad
        bordered.append(border_char + padded + border_char)
    for _ in range(padding):
        bordered.append(border_char + " " * inner_width + border_char)
    bordered.append(border_char * total_width)

    return bordered


def main():
    parser = argparse.ArgumentParser(description="Generate ASCII art banners from text")
    parser.add_argument("text", nargs="?", help="Text to convert to ASCII art")
    parser.add_argument(
        "--font", "-f",
        default="block",
        choices=list(FONTS.keys()),
        help="Font style (default: block)",
    )
    parser.add_argument("--border", "-b", action="store_true", help="Add a border")
    parser.add_argument(
        "--border-char",
        default="*",
        help="Character for border (default: *)",
    )
    parser.add_argument("--output", "-o", default=None, help="Save output to file")
    parser.add_argument("--list-fonts", action="store_true", help="List available fonts")

    args = parser.parse_args()

    if args.list_fonts:
        print("Available fonts:\n")
        for name, desc in FONT_DESCRIPTIONS.items():
            print(f"  {name:10s} - {desc}")
            sample = render_text("ABC", name)
            for line in sample:
                print(f"  {' ' * 12}{line}")
            print()
        sys.exit(0)

    if not args.text:
        parser.error("text is required (unless using --list-fonts)")

    lines = render_text(args.text, args.font)

    if args.border:
        lines = add_border(lines, args.border_char)

    output = "\n".join(lines)
    print(output)

    if args.output:
        with open(args.output, "w") as f:
            f.write(output + "\n")
        print(f"\nSaved to: {args.output}", file=sys.stderr)


if __name__ == "__main__":
    main()
